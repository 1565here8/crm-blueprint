const priorityCodes = new Set(CRM_PRIORITY_COUNTRIES.map((c) => c.code));
const merged = [...CRM_PRIORITY_COUNTRIES];
for (const c of ALL_COUNTRIES_RAW) {
  if (!priorityCodes.has(c.code)) merged.push(c);
}
for (const c of CRM_PRIORITY_COUNTRIES) {
  if (!ALL_COUNTRIES_RAW.some((x) => x.code === c.code) && !merged.some((x) => x.code === c.code)) {
    merged.push(c);
  }
}

/** Full list: priority countries first, then the rest alphabetically. */
export const CRM_COUNTRIES: CrmCountry[] = [
  ...CRM_PRIORITY_COUNTRIES,
  ...ALL_COUNTRIES_RAW.filter((c) => !priorityCodes.has(c.code)).sort((a, b) =>
    a.label.localeCompare(b.label),
  ),
];

const LABEL_BY_CODE = new Map<string, string>(
  CRM_COUNTRIES.map((c) => [c.code.toUpperCase(), c.label]),
);
LABEL_BY_CODE.set("UK", "United Kingdom");

export function countryLabel(code: string | null | undefined): string {
  if (!code?.trim()) return "—";
  const u = code.trim().toUpperCase();
  return LABEL_BY_CODE.get(u) ?? code.trim();
}
